# Create a list of fruits
fruits = ["apple", "banana", "cherry", "orange"]

# Loop through the list and print each fruit
for fruit in fruits:
    print(fruit)

# Add a new fruit to the list
fruits.append("kiwi")
print(fruits)

# Get the length of the list
print(len(fruits))

# Remove the second fruit from the list
fruits.pop(1)
print(fruits)
